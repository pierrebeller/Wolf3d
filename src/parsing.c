void	build_floor(t_wolf *e)
{
	int x;
	int y;

	y = 0;
	e->map = (int **)ft_x_malloc(sizeof(int *) * 16);
	while (y <= 15)
	{
		x = 0;
		e->map[y] = (int *)ft_x_malloc(sizeof(int) * 16);
		while (x < 15)
		{
			e->map[y][x] = 0;
			x++;
		}
		y++;
	}
}

void	parsing(t_wolf *e)
{
	build_floor(e);
	build_map(e);
	e->map_width = 15;
	e->map_heigth = 15;
}
